package com.mujdell2019.hackathon.models.db;

import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAttribute;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;
import com.fasterxml.jackson.databind.JsonNode;
import com.fasterxml.jackson.databind.ObjectMapper;
import com.fasterxml.jackson.databind.node.ObjectNode;
import com.mujdell2019.hackathon.models.IMarshal;
import com.mujdell2019.hackathon.models.db.product.ProductFeaturesDBModel;

@DynamoDBTable(tableName = "dellProducts")
public class DellProductDBModel implements IMarshal {

	/* Data Members */
	
	private String id;
	private String name;
	private int price;
	private String imageUrl;
	private double discount;  // initial value = 0.0 %
	private ProductFeaturesDBModel features;
	
	
	/* Constructors */
	
	public DellProductDBModel() {
		features = new ProductFeaturesDBModel();
	}
	
	public DellProductDBModel(String id) {
		this();
		this.id = id;
	}
	
	public DellProductDBModel(String id, String name, int price, String imageUrl, double discount, ProductFeaturesDBModel features) {
		this.id = id;
		this.name = name;
		this.price = price;
		this.imageUrl = imageUrl;
		this.discount = discount;
		this.features = features;
	}
	
	
	/* Getters and Setters */

	@DynamoDBHashKey(attributeName = "id")
	@DynamoDBAutoGeneratedKey
	public String getId() { return id; }
	public void setId(String id) { this.id = id; }

	@DynamoDBAttribute(attributeName = "name")
	public String getName() { return name; }
	public void setName(String name) { this.name = name; }

	@DynamoDBAttribute(attributeName = "price")
	public int getPrice() { return price; }
	public void setPrice(int price) { this.price = price; }
	
	@DynamoDBAttribute(attributeName = "imageUrl")
	public String getImageUrl() { return imageUrl; }
	public void setImageUrl(String imageUrl) { this.imageUrl = imageUrl; }

	@DynamoDBAttribute(attributeName = "discount")
	public double getDiscount() { return discount; }
	public void setDiscount(double discount) { this.discount = discount; }

	@DynamoDBAttribute(attributeName = "features")
	public ProductFeaturesDBModel getFeatures() { return features; }
	public void setFeatures(ProductFeaturesDBModel features) { this.features = features; }
	
	
	/* JSON Marshal Method */
	
	@Override
	public JsonNode marshal() {
		
		ObjectMapper objectMapper = new ObjectMapper();
		JsonNode result = objectMapper.createObjectNode();
		
		((ObjectNode) result).put("id", getId());
		((ObjectNode) result).put("name", getName());
		((ObjectNode) result).put("price", getPrice());
		((ObjectNode) result).put("imageUrl", getImageUrl());
		((ObjectNode) result).put("discount", getDiscount());
		((ObjectNode) result).set("features", getFeatures().marshal());
		
		return result;
	}
}
